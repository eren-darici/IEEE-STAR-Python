a  = int(input("Bir sayı giriniz: "))
b = int(input("Başka bir sayı daha giriniz: "))

#if

if a > b:
  print("a, b'den büyüktür.")

#elif (else if)

if a > b:
  print("a, b'den büyüktür.")
elif a == b:
  print("a, b'ye eşittir")


#else

if a > b:
  print("a, b'den büyüktür.")
elif a == b:
  print("a, b'ye eşittir")
else:
  print("a, b'den küçüktür.")
  