# Veri Tipleri type()

a = 3 # Integer
b = 5.8 # Float
c = "STAR" # String
d = True # Boolean

# Float --> Integer
b = int(b)
print(b)

# Integer --> Float
a = float(a)
print(a)

# String --> Number
c = int(c)
print(c)

c = float(c)
print(c)

# Number --> String
b = str(b)
print(b)

a = str(a)
print(a)